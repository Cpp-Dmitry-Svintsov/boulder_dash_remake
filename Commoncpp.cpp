AGame_Object::~AGame_Object()
{
}
AGame_Object::AGame_Object(int x, int y, EObject_Type type)
	:Cx(x), Cy(y), Mooving_State(EMooving_State::EMS_None ), Direction(0), Object_Type(type)
{
	Rx = 0.0;
	Ry = 0.0;
}
//=================================================================================================================
void AGame_Object::Get_Pos(double& cx, double& cy, double& rx, double& ry)
{
	cx = Cx;
	cy = Cy;
	rx = Rx;
	ry = Ry;
}
void AGame_Object::Set_Pos(double cx, double cy, double rx, double& ry)
{
	Cx = cx;
	Cy = cy;
	Rx = rx;
	Ry = ry;
}
//=================================================================================================================
void AGame_Object::Get_Ceil_Pos(int& x, int& y)
{
	x = Cx;
	y = Cy;
}
//=================================================================================================================
EObject_Type AGame_Object::Get_Type()
{
	return Object_Type;
}
